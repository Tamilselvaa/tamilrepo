import java.util.Scanner;
public record relationaloperator() {

    public static void main(String[] args){
        int a = 10 , b = 25 ;
        System.out.println("a==b:"+(a==b));
        System.out.println("a!=b:"+(a!=b));
        System.out.println("a>b:"+(a>b));
        System.out.println("a<b:"+(a<b));
        System.out.println("a>=b:"+(a>=b));  
        System.out.println("a<=b:"+(a<=b));
    }
    
}


